pending_replies = {}

MAX_LEN = 4500

def split_message(text):
    if len(text) <= MAX_LEN:
        return [text]
    parts = []
    while len(text) > MAX_LEN:
        split_pos = text[:MAX_LEN].rfind('\n')
        if split_pos == -1:
            split_pos = MAX_LEN
        parts.append(text[:split_pos].strip())
        text = text[split_pos:].strip()
    if text:
        parts.append(text)
    return parts

